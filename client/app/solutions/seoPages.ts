export function getSeoSolutionPage(slug: string) {
  return seoSolutionPages.find((page) => page.slug === slug);
}

export function buildSolutionMetadata(page: SeoSolutionPage): Metadata {
  return {
    title: `${page.title} | Rendlify`,
    description: page.description,
    keywords: page.keywords,
    alternates: {
      canonical: `/solutions/${page.slug}`,
    },
    openGraph: {
      title: `${page.title} | Rendlify`,
      description: page.description,
      url: `/solutions/${page.slug}`,
      siteName: 'Rendlify',
      locale: 'ru_RU',
      type: 'website',
    },
    twitter: {
      card: 'summary_large_image',
      title: `${page.title} | Rendlify`,
      description: page.description,
    },
  };
}
